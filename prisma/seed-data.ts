export function buildSeedQuestions() {
  const items: Array<{ category: string; questionText: string; displayOrder: number }> = [];
  let order = 1;

  for (const category of CATEGORY_DEFINITIONS) {
    const templates = QUESTION_TEMPLATES[category.key] ?? [];
    for (let i = 0; i < category.count; i++) {
      items.push({
        category: category.key,
        questionText:
          templates[i] ?? `${category.label}に関する設問${i + 1}`,
        displayOrder: order,
      });
      order += 1;
    }
  }

  return items;
}
